                                <h4>Limpieza facial</h4>
                                <p style="color:gray;padding-right: 4%;">Se realiza una limpieza profunda exfoliando la piel con puntas de diamante para así retirar impurezas, excesos de grasa, maquillaje, y células muertas; se activa el colageno y la elastina de la piel con ayuda de mascarillas nutritivas. </p>
                            </div>
                        </div>
