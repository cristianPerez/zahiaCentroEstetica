                                <h4>Peeling facial</h4>
                                <p style="color:gray;padding-right: 4%;">Terapia para manchas faciales por exposición solar, embarazo o tratamientos hormonales con Cosmelan, único e innovador tratamiento que inhibe la melanogénesis, evitando desde el comienzo la producción de meloncitos responsables de la formación de las manchas, ayuda también a la rehidratación de la piel y eliminación de exceso grasa.</p>
                            </div>
                        </div>
                        <div id="9" class="row-fluid" style="border-bottom: 2px solid #D8D8D8;">
                            <div class="span10">
                                <h4> Radiofrecuencia (flaccidez)</h4>
                                <p style="color:gray;padding-right: 4%;">Rejuvenecimiento y reafirmación facial con tecnología de radiofrecuencia para la activación de colágeno y elastina que reafirma la piel del rostro y redefine el contorno facial, disminuyendo la papada y las líneas de expresión.</p>
                            </div>
                        </div>
