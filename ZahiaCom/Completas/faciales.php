                                <h4>Hilos tensores (HILOS RUSOS)</h4>
                                <p style="color:gray;padding-right: 4%;">Los hilos Rusos son un procedimiento no quirúrgico lo mas aproximado a un estiramiento facial, que utilizan unos hilos insertados abajo de la piel que forman una estructura de apoyo para los tejidos. Este procedimiento es seguro, rápido y fácil de realizar, se lleva a cabo con anestesia local, el procedimiento se realiza aproximadamente en 45 minutos. Algunos de los resultados se podrán ver inmediatamente pero el resultado final es obtenido aproximadamente de 6 a 8 semanas, el dolor es mínimo, sin cicatrices, con una rápida recuperación y pueden regresar a sus actividades normales en menos de una semana.</p>
                            </div>
                        </div>
