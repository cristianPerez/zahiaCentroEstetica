                                <h4>Mesoporacion</h4>
                                <p style="color:gray;padding-right: 4%;">Su principio se basa en el aprovechamiento de los campos electromagnéticos para introducir sustancias directamente al interior celular. Brinda una excelente relación de rendimiento y permite la penetración de principios activos de forma localizada sin dolor ni agujas.</p>
                            </div>
                        </div>
                        <div id="11" class="row-fluid">
                            <div class="span10">
                                <h4>Alta frecuencia</h4>
                                <p style="color:gray;padding-right: 4%;">El alta frecuencia es un aparato de alta tecnología que produce ozono, el cual es antiséptico, astringente, bactericida, ataca los lípidos de la superficie de la piel, vasodilatación, estimula la producción de colageno, como resultado de todas estas propiedades es usado para el manejo de piel seborrea a (grasa), alopecia (calvicie), y acné.</p>
                            </div>
                        </div>
