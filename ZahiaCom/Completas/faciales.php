                                <h4>Plasma rico en plaquetas (PRP)</h4>
                                <p style="color:gray;padding-right: 4%;">El plasma rico en plaquetas, es rico en factores de crecimiento; el procedimiento consiste en extraer sangre del propio paciente separar sus componentes y por untimo aplicar en forma de msoterapia en el area atratar con el fin de renovar la piel del dándole una reactivación celular progresiva.</p>
