                        <div id="3" class="row-fluid" style="border-bottom: 2px solid #D8D8D8;">
                            <div class="span12">
                                <h4>Carboxiterapia</h4>
                                <p style="color:gray;padding-right: 4%;">La carboxiterapia es un gas de CO2 que estimula el flujo sanguineo, con diversas consecuencias: mejora la elasticidad de la piel, la reduccion de la celulitis, la reduccion de la grasa localizada y la produccion de colageno; esta indicada en Reduccion de Celulitis,Grasa localizada y la reduccion de peso con o sin laserlipolisis, Solucion de liposuccion complementaria para mejorar las irregularidades (fibrosis), Alopecia, Enfermedades arteriales asociados con la diabetes, Ulceras en la piel, Las estrias, Las cicatrices de acne y la laxitud de la piel (brazos, pecho, ...), La reduction de Arrugas</p>
                            </div>
                        </div>
                        <div id="4" class="row-fluid" style="border-bottom: 2px solid #D8D8D8;">
                            <div class="span12">
                                <h4>Masajes Reductores</h4>
                                <p style="color:gray;padding-right: 4%;">El masaje reductivo elimina la acumulación de grasa localizada y estiliza el contorno de la figura, es decir, crea una silueta más estética, tienen incidencia sobre el tejido conjuntivo, y además ayudan a una mejor irrigación y drenaje linfático e intravenoso</p>
                            </div>
                        </div>
                        <div id="5" class="row-fluid" style="border-bottom: 2px solid #D8D8D8;">
                            <div class="span12">
                                <h4>Mesoterapia adelgazante </h4>
                                <p style="color:gray;padding-right: 4%;">La Mesoterapia consiste en inyectar sustancias que disuelven la grasa corporal, estas sustancias son una mezcla de productos alopaticos y homeopaticos, minerales, vitaminas y aminoácidos, según lo que necesite el paciente, mejora notoriamente el contorno de glúteos, muslos, abdomen,caderas, rostro o cualquier zona del cuerpo donde sea aplicada. Además de eliminar la grasa las sustancias utilizadas activan la circulación sanguínea y linfática con lo cual se pueden corregir problemas de inflamación.</p>
                            </div>
                        </div>
                        <div id="6" class="row-fluid" style="border-bottom: 2px solid #D8D8D8;">
                            <div class="span12">
                                <h4>Cavitacion</h4>
                                <p style="color:gray;padding-right: 4%;">Moldeamiento corporal y adelgazamiento con equipo de última tecnología CAVIT con el que perderás centímetros de grasa localizada en cada sesión, de forma eficaz, indolora y segura, moldeando tu cuerpo de forma progresiva</p>
                            </div>
                        </div>
                        <div id="7" class="row-fluid" style="border-bottom: 2px solid #D8D8D8;">
                            <div class="span12">
                                <h4>Hidrolipoclasia Ultrasonica</h4>
                                <p style="color:gray;padding-right: 4%;">La hidrolipoclasia consiste en aplicación de infiltraciones de un cóctel de solución salina con varios medicamentos biológicos con propiedades reductoras, se aplica en las adiposidades localizadas; el éxito está en la terapia Ultrasónica que, entre otras cosas, produce termogénesis, o sea, genera calor y quema grasas. También rompe la fibrosis, un componente importante de la piel de naranja (celulitis).</p>
                            </div>
                        </div>
                        <div id="8" class="row-fluid" style="border-bottom: 2px solid #D8D8D8;">
                            <div class="span12">
                                <h4>Escleroterapia con espuma (varices)</h4>
                                <p style="color:gray;padding-right: 4%;">Consiste en introducir en las varices un producto quimíco conocido (espuma, esclerosante líquido), que irrita la cara interna de la misma y provoca una fibrosis en la variz, que lleva a “colapsar” y “secar” las varices, haciéndolas desaparecer.</p>
                            </div>
                        </div>
                        <div id="9" class="row-fluid" style="border-bottom: 2px solid #D8D8D8;">
                            <div class="span12">
                                <h4>Rejuvenecimiento de manos</h4>
                                <p style="color:gray;padding-right: 4%;">Por medio de diferentes tratamientos que son de libre elección por el paciente, entre ellos mesoterapia de medicamentos biológicos, radiofrecuencia, plasma rico en plaquetas, láser IPL, entre otros.</p>
                            </div>
                        </div>
                        <div id="10" class="row-fluid" style="border-bottom: 2px solid #D8D8D8;">
                            <div class="span12">
                                <h4>Electrosimulacion(CORRIENTES RUSAS)</h4>
                                <p style="color:gray;padding-right: 4%;">Moldeamiento y tonificación de músculos corporales con bioestimulación a través de corrientes rusas.</p>
                            </div>
                        </div>
                        <div id="11" class="row-fluid" style="border-bottom: 2px solid #D8D8D8;">
                            <div class="span12">
                                <h4>Radiofrecuencia (flaccidez)</h4>
                                <p style="color:gray;padding-right: 4%;">genera un intenso calor en la capa media de la dermis provocando de esta forma contracción de las fibras de colágeno. Esta contracción lleva a un acortamiento en la longitud de estas fibras y por lo tanto permite así elevación de tejidos flácidos o y/o caídos. Además el calentamiento generado estimula a los fibroblastos de la dermis a fabricar fibras de colágeno y elastina, engrosando la dermis y mejorando más aún el resultado obtenido.</p>
                            </div>
                        </div>
                        <div id="12" class="row-fluid" style="border-bottom: 2px solid #D8D8D8;">
                            <div class="span12">
                                <h4>Biomodelacion glutea</h4>
                                <p style="color:gray;padding-right: 4%;">La Biomodelación glutea o también llamada cirugía plástica sin cortes, es una opción no quirúrgica que consiste en rellenar algunas partes del cuerpo con sustancias orgánicas.</p>
                            </div>
                        </div>
                        <div id="13" class="row-fluid">
                            <div class="span12">
                                <h4>Tonificación glutea con vitamina C</h4>
                                <p style="color:gray;padding-right: 4%;">la vitamina C, la cual tiene propiedades para estimular la producción de colágeno del cuerpo, combatir el envejecimiento de la piel, las estrías, las manchas ocasionadas por la edad, la flacidez y los efectos de los rayos ultravioleta,Un tratamiento que produce resultados sorprendentes a los cuatro meses, pero que debe acompañarse con tratamientos estéticos adicionales o masajes reductores.</p>
